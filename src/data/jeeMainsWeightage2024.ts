// Calculate total weightage per subject
export const getTotalWeightage = (subject: keyof typeof JEE_MAINS_2024_WEIGHTAGE) => {
    const subjectData = JEE_MAINS_2024_WEIGHTAGE[subject];
    let total = 0;

    Object.values(subjectData).forEach((classData: any) => {
        Object.values(classData).forEach((unit: any) => {
            total += unit.weight;
        });
    });

    return total;
};

// Get question distribution based on weightage
export const getWeightageDistribution = (
    subject: keyof typeof JEE_MAINS_2024_WEIGHTAGE,
    totalQuestions: number
) => {
    const subjectData = JEE_MAINS_2024_WEIGHTAGE[subject];
    const totalWeight = getTotalWeightage(subject);
    const distribution: Record<string, number> = {};

    Object.entries(subjectData).forEach(([className, classData]) => {
        Object.entries(classData as Record<string, any>).forEach(([unitName, unitData]: [string, any]) => {
            const questions = Math.round((unitData.weight / totalWeight) * totalQuestions);
            distribution[`${className} - ${unitName}`] = questions;
        });
    });

    return distribution;
};
